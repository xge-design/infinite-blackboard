/**
 * 生成 UUID v4（使用种子 PRNG）。
 */
function generateId(rng: () => number): string {
  const hex = () => Math.floor(rng() * 16).toString(16);
  return (
    Array.from({ length: 8 }, hex).join('') +
    '-' +
    Array.from({ length: 4 }, hex).join('') +
    '-4' +
    Array.from({ length: 3 }, hex).join('') +
    '-' +
    ['8', '9', 'a', 'b'][Math.floor(rng() * 4)] +
    Array.from({ length: 3 }, hex).join('') +
    '-' +
    Array.from({ length: 12 }, hex).join('')
  );
}

/**
 * 生成 2020 至今的随机日期。
 */
function randomDate(rng: () => number): string {
  const start = new Date('2020-01-01').getTime();
  const end = Date.now();
  return new Date(start + rng() * (end - start)).toISOString();
}

/**
 * 生成所有示例消息。
 *
 * @param count 消息总数（默认 500）
 * @param seed  PRNG 种子
 */
export function generateSampleMessages(
  count: number = 500,
  seed: number = 42
): BlackboardMessage[] {
  const rng = mulberry32(seed);
  const messages: BlackboardMessage[] = [];

  // 自然网格——疏松、不整齐
  const cols = Math.ceil(Math.sqrt(count * 2.5));
  const cellW = CANVAS.CARD_WIDTH + CANVAS.GRID_GAP;
  const cellH = CANVAS.CARD_HEIGHT + CANVAS.GRID_GAP;
  const totalW = cols * cellW;
  const totalRows = Math.ceil(count / cols);
  const totalH = totalRows * cellH;
  const startX = -totalW / 2;
  const startY = -totalH / 2;

  for (let i = 0; i < count; i++) {
    const col = i % cols;
    const row = Math.floor(i / cols);

    // 大幅度随机偏移——模拟真实书写位置
    const jitterX = (rng() - 0.5) * cellW * 0.7;
    const jitterY = (rng() - 0.5) * cellH * 0.7;
    const x = startX + col * cellW + cellW / 2 + jitterX;
    const y = startY + row * cellH + cellH / 2 + jitterY;

    // 微小旋转——手写感
    const rotation = (rng() - 0.5) * 6; // -3° ~ +3°

    const wish = WISHES[Math.floor(rng() * WISHES.length)];
    const content = `${PROMPT_PREFIX}${wish}${PROMPT_SUFFIX}`;

    messages.push({
      id: generateId(rng),
      content,
      position: { x, y },
      color: randomChalkColor(),
      opacity: 0.6 + rng() * 0.35,
      fontSize: 38 + rng() * 16,
      rotation,
      language: 'zh',
      createdAt: randomDate(rng),
      embedding: null,
      nearestNeighbors: [],
      emotions: null,
      topicCluster: null,
    });
  }

  // 洗牌（Fisher-Yates）
  for (let i = messages.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    [messages[i], messages[j]] = [messages[j], messages[i]];
  }

  return messages;
}

